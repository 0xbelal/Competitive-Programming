bool can(ll mid,vector<ll> &a,ll x){
   ll water = 0;
    for (auto height : a) {
        if (mid > height) {
            water += (mid - height);
            if (water > x) return false; 
        }
    }
    return water <= x;
}
void solve() {

   /* 
      w is monotonic funcation if we increase h we need more water
   */
  ll n,x;cin>>n>>x;
  vector<ll> a(n);cin>>a;

   ll l = 1, r = 1e18;
    ll ans = 1;

    while(l<=r){
      ll mid = l + (r-l)/2;

      if(can(mid,a,x)){
         ans=mid;
         l = mid +1;
      }else{
         r=mid-1;
      }
    }
  
    cout<<ans<<"\n";
    
}

