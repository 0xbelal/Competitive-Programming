const double EPS = (1e-7);
vector<vector<char>> grid(50, vector<char>(50));

void pre() {
    bool start = true;

    for (int i = 0; i < 40; i += 2) {
        bool cur = start;
        for (int j = 0; j < 40; j += 2) {
            char c = (cur ? '#' : '.');

            grid[i][j] = c;
            grid[i][j + 1] = c;
            grid[i + 1][j] = c;
            grid[i + 1][j + 1] = c;
            cur = !cur; 
         }
        start = (start) ? false : true;
    }
}

void solve() {
    int n; cin >> n;
    int size = 2 * n;
    for (int i = 0; i < size; ++i) {
        for (int j = 0; j < size; ++j) {
            cout << grid[i][j];
        }
        cout << '\n';
    }
}

int main() {
    fast_io;

    pre();
    int t = 1;

    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
