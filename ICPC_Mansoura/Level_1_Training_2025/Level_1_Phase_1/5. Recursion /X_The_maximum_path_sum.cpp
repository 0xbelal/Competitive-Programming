int n,m;
vector<vector<int>> grid;


int max_sum(int i,int j){

    if(i == n - 1 &&  j == m - 1){
        return grid[i][j];
    }


    int option1 = INT_MIN,option2=INT_MIN;

    if(i+1 < n)
        option1 = grid[i][j] + max_sum(i+1,j);
    if(j + 1 < m)
        option2 = grid[i][j] + max_sum(i,j+1);

    return max(option1,option2);
}
