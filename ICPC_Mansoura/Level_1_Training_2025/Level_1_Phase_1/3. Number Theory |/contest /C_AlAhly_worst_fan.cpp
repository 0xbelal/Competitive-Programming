void solve() {

   int n;
        cin >> n;
 
        vector<int> arr(n);cin>>arr;
 
        vector<int> prefix_min(n);
        prefix_min[0] = arr[0];
        for (int i = 1; i < prefix_min.size(); ++i) {
            prefix_min[i] = min(prefix_min[i - 1], arr[i]);
        }
 
        vector<int> suffix_max(n);
        suffix_max[n-1] = arr[n-1];
        for (int i = n - 2; i >= 0; --i) {
            suffix_max[i] = max(suffix_max[i + 1], arr[i]);
        }
 
        string result = "";
        for (int i = 0; i < arr.size(); ++i) {
            if (arr[i] == prefix_min[n-1] || arr[i] == suffix_max[0] ||
                (arr[i] == prefix_min[i] && arr[i] <= suffix_max[i]) ||
                (arr[i] >= prefix_min[i] && arr[i] == suffix_max[i])) {
                result += "1";
            } else {
                result += "0";
            }
        }
        cout << result << '\n';
}

