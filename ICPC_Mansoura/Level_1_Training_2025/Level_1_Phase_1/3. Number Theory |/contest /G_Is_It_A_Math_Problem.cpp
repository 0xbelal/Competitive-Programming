ll no_of_divisors(ll n){
   ll cntr=0;

   for(ll i=1;i*i<=n;i++){
      if(n%i == 0) cntr+=2;
      if(i*i == n) cntr--;
   }
   return cntr;
} 

void solve() {

   ll n;cin>>n;
   ll no_div = no_of_divisors(n);
   ll a,b;
   if(no_div%2==0){
      a=n;
      b=no_div/2;
   }else{
      a=sqrt(n); 
      b=no_div;
      /*
         16--- 1,2,4,8,16
         16^2 * 4
         (2^4)^2 * 2^2 
         2^10
      */
   }
   cout<<a<<" "<<b;
}

