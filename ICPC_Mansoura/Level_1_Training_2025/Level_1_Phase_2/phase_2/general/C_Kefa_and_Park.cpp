const int N = 200000 + 5;
int n,m,ans;
vector<int> arr[N];
bool  vis[N],cats[N];

bool valid(int cost){
   return ( cost <= m);
}

void dfs(int node,int cost){
   vis[node] =true;
   bool flag=true;
   for(int i:arr[node]){
      if(!vis[i] ){
         flag=false;
         int curr_cost = ((cats[i] == 1)? cost + 1 : 0 );
         if (valid(curr_cost)) {
            dfs(i, curr_cost);
         }
      }
   }

   if (flag) ans++;
}
void solve() {

   cin>>n>>m;
   for(int i=1;i<=n;i++) cin>>cats[i];
   for(int i=0;i<n-1;i++){
      int x,y;cin>>x>>y;
      arr[x].push_back(y);
      arr[y].push_back(x);
   }
   dfs(1,cats[1]);
   cout<<ans;

    
}

