const double EPS = (1e-7);
const int N=10000+1;
int n,m;
vector<int> nodes[N];
int vis[N];
int not_cycle=1;
int ans;

void dfs(int u,int p){
   vis[u]=1;
   ans++;
   for(int chil : nodes[u]){
      if(!vis[chil]){
          dfs(chil,u);
      }else if(chil != p){
         not_cycle=0;
      }
   }
}
void solve() {

   cin>>n>>m;
   if (m != n-1) {
      cout << "NO";
      return;
   }
   while(m--){
      int x,y;cin>>x>>y;
      nodes[x].push_back(y);
      nodes[y].push_back(x);
   }

   dfs(1,-1);

   cout<< ( ans==n && (not_cycle ) ? "YES" : "NO");
    
}

