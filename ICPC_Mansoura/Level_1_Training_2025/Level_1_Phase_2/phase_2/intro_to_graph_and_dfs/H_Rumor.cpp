const double EPS = (1e-7);

const int N=1e5+5;
int n,m;
vector<int> adj[N];
int vis[N],gold[N];
ll ans;


void dfs(int u,int &min_gold){
   vis[u]=1;
   for(int v:adj[u]){
      if(!vis[v]) {
         min_gold = min(min_gold,gold[v]);
         dfs(v,min_gold);
      }
   }
}
void solve() {



   /*
      - to get min amount of gold to spread the rumor in each seprate component
      > start from the node that required min amount of gold
   */


   cin>>n>>m;
   for(int i=1;i<=n;i++)cin>>gold[i];
   while(m--){
      int x,y;cin>>x>>y;
      adj[x].push_back(y);
      adj[y].push_back(x);
   }
   int min_gold=__INT_MAX__,res=__INT_MAX__;

   for(int i=1;i<=n;i++){
      if(!vis[i]){
         min_gold=gold[i];
         dfs(i,min_gold);
         res = min(res,min_gold);
         ans+=res;
         res=__INT_MAX__;
      }
   }
   
   cout<<ans;
    
}

