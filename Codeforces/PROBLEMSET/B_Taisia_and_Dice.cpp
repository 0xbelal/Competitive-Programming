void solve() {

   int n,s,r;cin>>n>>s>>r;

   int max_die = s - r; 

        vector<int> a;
        a.push_back(max_die); 

        int remaining = r;
        int dice_left = n - 1;

        for (int i = 0; i < dice_left; i++) {
            int put = min(max_die, remaining - (dice_left - (i + 1)) * 1);

            put = max(1, put);
            a.push_back(put);
            remaining -= put;
        }

        for (int x : a) cout << x << " ";
        cout << "\n";
    
}

