struct point{
    int x,y;
    double getDiff(point p){
        return sqrt(pow(this->x - p.x,2) + pow(this->y - p.y,2));
    }
};

void solve() {

    int n,k;cin>>n>>k;
    vector<point> ps(n);
    
    for(point &p : ps)cin >> p.x >> p.y;
    double dist = 0;
    for(int i = 0; i < n-1; i++){
        dist += ps[i].getDiff(ps[i+1]);
    }
    double ans = k * dist / 50.0;

    cout << fixed << setprecision(9) << ans;
}

